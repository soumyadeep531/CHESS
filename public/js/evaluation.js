const pieceValues = { p: 100, r: 500, n: 320, b: 330, q: 900, k: 20000 };

const evaluateBoard = (board) => {
    let totalEvaluation = 0;
    for (let i = 0; i < 8; i++) {
        for (let j = 0; j < 8; j++) {
            totalEvaluation += getPieceValue(board[i][j], i, j);
        }
    }
    return totalEvaluation;
};

const getPieceValue = (piece, x, y) => {
    if (piece === null) {
        return 0;
    }

    // x is row (0-7), y is col (0-7).
    // PST tables usually are from White's perspective (bottom to top).
    // board[0] is rank 8 (Black side), board[7] is rank 1 (White side).

    let absoluteValue = pieceValues[piece.type];

    // Add positional value
    if (piece.color === 'w') {
        absoluteValue += pst[piece.type][x][y];
        return absoluteValue;
    } else {
        // Mirror x (row): 7 - x. 
        absoluteValue += pst[piece.type][7 - x][y];
        return -absoluteValue;
    }
};
